
package com.ascencio.model;

public class Mensual {
    
    private int IDMEN;
    private String MESMEN;
    private int CANTINF;
    private int CANTREC;
    private int CANTMUE;

    public int getIDMEN() {
        return IDMEN;
    }

    public void setIDMEN(int IDMEN) {
        this.IDMEN = IDMEN;
    }

    public String getMESMEN() {
        return MESMEN;
    }

    public void setMESMEN(String MESMEN) {
        this.MESMEN = MESMEN;
    }

    public int getCANTINF() {
        return CANTINF;
    }

    public void setCANTINF(int CANTINF) {
        this.CANTINF = CANTINF;
    }

    public int getCANTREC() {
        return CANTREC;
    }

    public void setCANTREC(int CANTREC) {
        this.CANTREC = CANTREC;
    }

    public int getCANTMUE() {
        return CANTMUE;
    }

    public void setCANTMUE(int CANTMUE) {
        this.CANTMUE = CANTMUE;
    }

    
}
