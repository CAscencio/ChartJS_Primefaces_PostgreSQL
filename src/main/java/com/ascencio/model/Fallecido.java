
package com.ascencio.model;

public class Fallecido {
    
    private int IDFALL;
    private String TIPFALL;
    private int CANTFALL;

    public int getIDFALL() {
        return IDFALL;
    }

    public void setIDFALL(int IDFALL) {
        this.IDFALL = IDFALL;
    }

    public String getTIPFALL() {
        return TIPFALL;
    }

    public void setTIPFALL(String TIPFALL) {
        this.TIPFALL = TIPFALL;
    }

    public int getCANTFALL() {
        return CANTFALL;
    }

    public void setCANTFALL(int CANTFALL) {
        this.CANTFALL = CANTFALL;
    }
    
}
